function addNum(num1, num2) {
    result = num1 + num2;
    return result;
}

function subtractNum(num1, num2) {
    result = num1 - num2;
    return result;
}

function multiplyNum(num1, num2) {
    result = num1 * num2;
    return result;
}

function divideNum(num1, num2) {
    result = num1 / num2;
    return result;
}

console.log(addNum(2,2));
console.log(subtractNum(4,2));
console.log(multiplyNum(2,2));
console.log(divideNum(4,2));
